    public class HiloHerencia extends Thread {

        Vista vista;

        private HiloHerencia(Vista vista) {
            this.vista = vista;
        }

        public void run() {
            while (true) {
                this.vista.jl_tiempo_encendido.setText(Comando.tiempoEncendio());
            }
        }
    }

    public class HiloImplements implements Runnable {

        Vista vista;

        private HiloImplements(Vista vista) {
            this.vista = vista;
        }

        public void run() {
            while (true) {
                this.vista.jl_temperatura_cpu.setText(Comando.temperatura());
            }
        }

    }

    HiloHerencia tiempo_encendido;
    HiloImplements hiloRunnable;
    Thread temperatura_cpu;
    Thread memoria;

