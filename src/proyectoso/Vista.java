    private void tiempoEncendido() {
        this.tiempo_encendido = new HiloHerencia(this);
    }

    private void temperaturaCPU() {
        hiloRunnable = new HiloImplements(this);
        temperatura_cpu = new Thread(hiloRunnable);
    }

    private void memoria() {
        memoria = new Thread() {
            public void run() {
                while (true) {
                    jl_memoria.setText(Comando.memoria());
                    try {
                        Thread.sleep(100);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        };

    }

